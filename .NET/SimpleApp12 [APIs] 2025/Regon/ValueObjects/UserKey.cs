namespace Regon.ValueObjects
{
    public record UserKey
    {
        //Properties
        public string Value { get; init; } = null!;


        //================================================================================================
        //================================================================================================
        //================================================================================================
        //Public Methods
        public static implicit operator string(UserKey value)
        {
            return value.Value;
        }

        public static implicit operator UserKey(string value)
        {
            return new UserKey
            {
                Value = value,
            };
        }

        //================================================================================================
        //================================================================================================
        //================================================================================================
        //Private Methods
    }
}
