namespace Regon.ValueObjects
{
    public record SessionId
    {
        //Parameters
        public string Value { get; init; } = null!;


        //===============================================================================================
        //===============================================================================================
        //===============================================================================================
        //Public Methods
        public static implicit operator string(SessionId value)
        {
            return value.Value;
        }

        public static implicit operator SessionId(string value)
        {
            return new SessionId
            {
                Value = value,
            };
        }
        //===============================================================================================
        //===============================================================================================
        //===============================================================================================
        //Private Methods
    }
}
